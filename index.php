            console.log("crt: " + crt_img);
            dist = (prev_posi - crt_img);

            console.log(wide.offsetWidth);

            if (wide.offsetWidth >= 1400) {
                dist_main += (dist * 100);
                document.getElementById("images").style.left = dist_main + "px";
                imgs[crt_img - 1].style.width = "390px";
            }
            if (wide.offsetWidth >= 1200 && wide.offsetWidth < 1400) {
                dist_main += (dist * 100);
                document.getElementById("images").style.left = dist_main + "px";
                imgs[crt_img - 1].style.width = "334px";
